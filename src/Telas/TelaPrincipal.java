    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
     
    	DefaultTableModel table = (DefaultTableModel) jTable1.getModel(); 
        for (int row = 0; row < table.getRowCount(); row++) {            
            Termo t = new Termo();       
            ControlaDicionario ctlDicionario = new ControlaDicionario();  

            t.setNumero(Integer.parseInt(table.getValueAt(row, 0).toString()));
            t.setTermo(table.getValueAt(row, 1).toString());
            t.setSignificado(table.getValueAt(row, 2).toString());
            t.setFonte(table.getValueAt(row, 3).toString());
            
            if (row == 0) {        
                if (!ctlDicionario.salvar(t, false)) { 
                    JOptionPane.showMessageDialog(null, "Erro, problemas ao SALVAR em arquivo!");
                }
            } else {              
                if (!ctlDicionario.salvar(t, true)) { 
                    JOptionPane.showMessageDialog(null, "Erro, problemas ao SALVAR em arquivo!");
                }                
            }
        }      

    }//GEN-LAST:event_jbSalvarActionPerformed

