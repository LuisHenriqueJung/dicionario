    private void jbExibirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExibirActionPerformed
   
        int row = -1;
        row = jTable1.getSelectedRow();
        if (row != -1) {
            String termo = jTable1.getValueAt(row, 1).toString();
            String signf = jTable1.getValueAt(row, 2).toString();
            String fonte = jTable1.getValueAt(row, 3).toString();
            if (termo.isEmpty() || signf.isEmpty() || fonte.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Sem dados para mostrar!");
            } else {
                jlExibeTermo.setText(termo);
                jtpSignificado.setText(signf);
                jtpFonte.setText(fonte);
                jdExibeDados.setVisible(rootPaneCheckingEnabled);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para exibir!");
        }


             
           
    }//GEN-LAST:event_jbExibirActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        if (row < 0){
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada!");
        }else{
            int resposta = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do termo selecionado na tabela?", "Excluir cadastro", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                table.removeRow(row);
            }
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

