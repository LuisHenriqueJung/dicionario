    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        Termo t  = new Termo();        
        ControlaDicionario ctlDicionario = new ControlaDicionario();        
        
        if ( (jtfNumero.getText().isEmpty()) || (jtfTermo.getText().isEmpty()) || (jtfSignificado.getText().isEmpty()) || (jtfFonte.getText().isEmpty()) ) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            jtfNumero.requestFocus();
            return;
        }
        
        String numeroAdc = jtfNumero.getText();      
        String termoAdc = jtfTermo.getText();
        String signAdc = jtfSignificado.getText();
        String fonteAdc = jtfFonte.getText();
        String conteudo = ctlDicionario.confereExis(numeroAdc,termoAdc); 
        if (conteudo == "X") {
            JOptionPane.showMessageDialog(null, "Este termo já se encontra em nosso cadastro!!");
          
            jtfNumero.requestFocus();   
            return;
        }        
        
        t.setNumero(Integer.parseInt(jtfNumero.getText()));
        t.setTermo(jtfTermo.getText());
        t.setSignificado(jtfSignificado.getText());
        t.setFonte(jtfFonte.getText());

        if (ctlDicionario.salvar(t, true)) { 
            JOptionPane.showMessageDialog(null, "Registro adicionado com SUCESSO ao arquivo!");

            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();   
            Object rowData[] = new Object[4]; 
            rowData[0] = jtfNumero.getText();
            rowData[1] = jtfTermo.getText();
            rowData[2] = jtfSignificado.getText();
            rowData[3] = jtfFonte.getText();
            table.addRow(rowData);   
            
            // limpa campos
            jtfNumero.setText("");
            jtfTermo.setText("");
            jtfSignificado.setText("");
            jtfFonte.setText("");

            // posiciona cursor para proxima insercao
            jtfNumero.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "Erro, problemas ao SALVAR em arquivo!");
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

